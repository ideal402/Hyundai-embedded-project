void dataSlice(volatile int data, volatile int i, volatile int limit) {
	if (i == limit)
		return ;
	
		aTxBuffer[i] = data % 10;
		data = data / 10;
		i--;
		dataSlice(data, i, limit);
}

void combineData(void) {
	volatile int data;
	for (volatile int i = 0; i < 5; i++) {	
		switch(i) {
			case 0:
				data = bVib;
				break;
			case 1: 
				data = light;
				break ;
			case 2:
				data = rot;
				break ;
			case 3:
				data = Humidity;
				break ;
			case 4:
				data = Temperature;
				break ;
			default:
				break ;
		}
		dataSlice(data, 14 - (i * 3), 14 - (i * 3) - 3);
	}
}


